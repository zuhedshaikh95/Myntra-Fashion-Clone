function displayItems(masterArr){
    container.innerHTML=""
    let count=0;
    masterArr.forEach((elem,index)=>{
        let div = document.createElement("div");
        let content = document.createElement("div");
        let brand = document.createElement("h4");
        let name = document.createElement("h5");
        let price = document.createElement("p");
        let strikedPrice = document.createElement("s");
        let disc = document.createElement("p");
        let img = document.createElement("img");
        let button = document.createElement("button");
        let icon = document.createElement("i");

        brand.innerText=elem.brand;
        name.innerText=elem.name;
        price.innerText="Rs."+elem.price;
        strikedPrice.innerText="Rs."+elem.strikedoffPrice;
        disc.innerHTML=`(${elem.discount}% OFF)`;
        button.append(icon,"WISHLIST");

        img.setAttribute("src",elem.images[0]);
        icon.setAttribute("class","fa-solid fa-heart");
        brand.setAttribute("class","brand");
        name.setAttribute("class","name");

        content.append(button,brand,name,price,strikedPrice,disc);
        div.append(img,content);
        
        container.append(div);
        count++;

        for(let i=0;i<wishArr.length;i++){
            if(wishArr[i].code == elem["code"]){
                button.innerHTML="";
                icon.style.color="#FF3F6C"
                button.append(icon,"WISHLISTED");
                button.style.backgroundColor="#535766";
                button.style.color="#fff";
                button.style.border="0";
                button.disabled="true";
            }
        }

        button.addEventListener("click",()=>{
            button.innerHTML="";
            icon.style.color="#FF3F6C"
            button.append(icon,"WISHLISTED");
            button.style.backgroundColor="#535766";
            button.style.color="#fff";
            button.style.border="0";
            button.disabled="true";
            elem.Qty=1;
            wishArr.push(elem);
            localStorage.setItem("wishlist",JSON.stringify(wishArr));
        });

        img.addEventListener("click",()=>{
            let foo=[elem];
            localStorage.setItem("mono",JSON.stringify(foo));
            foo=[];
            window.location.href="view.html";
        });
    });
    document.querySelector("#count").innerText=count;
};

